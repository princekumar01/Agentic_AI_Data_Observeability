# ─── Graph builder ────────────────────────────────────────────────────────────

def build_graph() -> StateGraph:
    sg = StateGraph(AgentState)

    # Register nodes
    sg.add_node("supervisor", supervisor_node)
    sg.add_node("data_quality", data_quality_node)
    sg.add_node("log_analysis", log_analysis_node)
    sg.add_node("rca", rca_node)
    sg.add_node("recommendation", recommendation_node)
    sg.add_node("compliance", compliance_node)
    sg.add_node("aggregator", aggregator_node)

    # Sequential edges
    sg.set_entry_point("supervisor")
    sg.add_edge("supervisor", "data_quality")
    sg.add_edge("data_quality", "log_analysis")
    sg.add_edge("log_analysis", "rca")
    sg.add_edge("rca", "recommendation")
    sg.add_edge("recommendation", "compliance")
    sg.add_edge("compliance", "aggregator")
    sg.add_edge("aggregator", END)

    return sg.compile()


# Module-level compiled graph
graph = build_graph()


# ─── Pipeline runner ─────────────────────────────────────────────────────────

def run_agent_pipeline(
    run_id: str,
    sanitized_metrics: Dict[str, Any],
    sanitized_log_text: str,
    streaming_metadata: Dict[str, Any],
    output_dir: str,
) -> AgentState:
    """
    Invoke the compiled LangGraph.
    Returns the final AgentState.
    """
    logger.info(f"[graph] Starting agent pipeline | run_id={run_id}")

    initial_state: AgentState = {
        "run_id": run_id,
        "sanitized_metrics": sanitized_metrics,
        "sanitized_log_text": sanitized_log_text,
        "streaming_metadata": streaming_metadata,
        "output_dir": output_dir,
        "data_quality_findings": "",
        "log_analysis_findings": "",
        "rca_findings": "",
        "recommendations": "",
        "compliance_review": "",
        "incident_report": "",
        "audit_entries": [],
        "error": None,
    }

    try:
        final_state = graph.invoke(initial_state)
        logger.info(f"[graph] Agent pipeline complete | run_id={run_id}")
        return final_state
    except Exception as exc:
        logger.error(f"[graph] Agent pipeline failed | run_id={run_id} | error={exc}")
        return {**initial_state, "error": str(exc)}
