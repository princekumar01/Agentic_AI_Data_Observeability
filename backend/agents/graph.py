"""
backend/agents/graph.py
─────────────────────────────────────────────────────
LangGraph state machine for the 5-agent pipeline.

Edge order (sequential):
  supervisor → data_quality → log_analysis → rca
              → recommendation → compliance → aggregator → END

Aggregator: deterministic Python, no LLM.
Assembles incident_report markdown from all five agent findings.
"""
from __future__ import annotations

import json
import logging
import os
from datetime import datetime, timezone
from typing import Any, Dict

from langgraph.graph import END, StateGraph  # type: ignore

from backend.agents.state import AgentState
from backend.agents.supervisor import supervisor_node
from backend.agents.data_quality_agent import data_quality_node
from backend.agents.log_analysis_agent import log_analysis_node
from backend.agents.rca_agent import rca_node
from backend.agents.recommendation_agent import recommendation_node
from backend.agents.compliance_agent import compliance_node

logger = logging.getLogger(__name__)


# ─── Aggregator (deterministic — no LLM) ────────────────────────────────────

def aggregator_node(state: AgentState) -> AgentState:
    """
    Assembles incident_report_draft.md from all five agent findings.
    Writes file to output_dir.
    Sets status to pending_review (caller handles final persistence).
    """
    run_id = state["run_id"]
    output_dir = state["output_dir"]
    metrics = state.get("sanitized_metrics", {})
    now = datetime.now(timezone.utc).isoformat()

    logger.info(f"[aggregator] Assembling incident report | run_id={run_id}")

    report = _build_report(
        run_id=run_id,
        metrics=metrics,
        dq=state.get("data_quality_findings", ""),
        log_a=state.get("log_analysis_findings", ""),
        rca=state.get("rca_findings", ""),
        rec=state.get("recommendations", ""),
        comp=state.get("compliance_review", ""),
        generated_at=now,
    )

    # Write incident_report_draft.md
    draft_path = os.path.join(output_dir, "incident_report_draft.md")
    os.makedirs(output_dir, exist_ok=True)
    with open(draft_path, "w", encoding="utf-8") as fh:
        fh.write(report)

    logger.info(f"[aggregator] incident_report_draft.md written to {draft_path}")

    return {**state, "incident_report": report}


